var inputArray = input.split('\n');
var colCount = inputArray[0].trim().length;

var steps = [
    {
        down: 1,
        right: 1    
    },
    {
        down: 1,
        right: 3    
    },
    {
        down: 1,
        right: 5    
    },
    {
        down: 1,
        right: 7    
    },
    {
        down: 2,
        right: 1    
    }
];
var hits = [];

steps.forEach(stepEl => {
    var colIndex = 0;
    var treesHit = 0;

    for (var row = stepEl.down; row < inputArray.length; row+= stepEl.down){
        colIndex += stepEl.right;
        colIndex %= colCount;
    
        if (inputArray[row].trim()[colIndex] == '#'){
            treesHit++;
        }
    }
    hits.push(treesHit);
});

var total = hits.reduce((a,b) => a*b);
console.log(total, ' trees hit on the way down');