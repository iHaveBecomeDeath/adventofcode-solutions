var lines = input.split('\n').map(l => l.trim());
var incomplete = [];
var scores = [];

lines.forEach(l => {
  var line = l;
  while (true) {
    var matches = line.match(/\[\]|\{\}|\<\>|\(\)/);
    if (!matches)
    {
      break;
    } 
    else 
    {
      line = line
        .replace('[]', '')
        .replace('{}', '')
        .replace('<>', '')
        .replace('()', '')
    }
  }
  var lchars = line.split('');
  
  if (lchars.filter(c => ['[', '(', '{', '<'].includes(c)).length == lchars.length) {
    incomplete.push([l, line]);
    // console.log('incomplete', l, line);
  } 
});

incomplete.forEach(i => {
  var sum = 0;
  i[1].split('').reverse().forEach(c =>
    {
      sum *= 5;
      switch (c) {
        case '(':
          sum += 1;
          break;
        case '[':
          sum += 2;
          break;
        case '{':
          sum += 3;
          break;
        case '<':
          sum += 4;
          break;                  
        default:
          break;
      }
    });
  scores.push(sum);
});

console.log(scores);
scores.sort((a, b) => a - b);
var middle = scores.length / 2;
var median = middle % 1 ? scores[middle - 0.5] : (scores[middle - 1] + scores[middle]) / 2;
console.log(median);
