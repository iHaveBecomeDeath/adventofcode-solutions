var lines = input.split('\n').map(l => l.trim());
var illegals = [];

lines.forEach(l => {
  var line = l;
  while (true) {
    var matches = line.match(/\[\]|\{\}|\<\>|\(\)/);
    if (!matches)
    {
      break;
    } 
    else 
    {
      line = line
        .replace('[]', '')
        .replace('{}', '')
        .replace('<>', '')
        .replace('()', '')
    }
  }
  var lchars = line.split('');
  
  if (!(lchars.filter(c => ['[', '(', '{', '<'].includes(c)).length == lchars.length)) {
    if (lchars.length){
      var matches = lchars.join('').match(/\]|\}|\>|\)/);
      if (matches)
        illegals.push(matches[0]);
    }
  }
});

var sum = illegals.map(i => {
  switch (i) {
    case ')':
      return 3;
      break;
    case ']':
      return 57;
      break;
    case '}':
      return 1197;
      break;
    case '>':
      return 25137;
      break;
    default:
      break;
  }
}).reduce((a,b) => a + b);

